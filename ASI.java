public class ASI {
    String AS;
    int mod;
    
    public String getAS() {
        return AS;
    }
    public int getmod() {
        return mod;
    }

    public void setAS(String AS) {
        this.AS = AS;
    }
    public void setmod(int mod) {
        this.mod = mod;
    }

    public ASI(String AS, int mod) {
        this.AS = AS;
        this.mod = mod;
    }
}
